class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid)
    {
        size_t m, n;
        m = obstacleGrid.size();
        if (m == 0)
            return 0;
        
        n = obstacleGrid[0].size();
        
        vector<long long> paths(n, 0);
        
        for (int i = 0; i < m; i++)
        {
            for (int j = 0; j < n; j++)
            {
                if (obstacleGrid[i][j] == 1)
                {
                    paths[j] = 0;
                }
                else
                {
                    if (i == 0)
                    {
                        if (j == 0)
                        {
                            paths[j] = 1;
                        }
                        else
                        {
                            paths[j] = paths[j - 1];
                        }
                    }
                    else
                    {
                        if (j != 0)
                        {
                            paths[j] = paths[j - 1] + paths[j];
                        }
                    }
                }
                
            }
        }
        
        return paths[n - 1];
    }
};

